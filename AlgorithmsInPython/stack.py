
# Stack implementation in Python


stack = []

# Add elements into stack
stack.append('a')
stack.append('b')
stack.append('c')

print('Stack')
print(stack)

# Popping elements from stack
print('\nAfter popping an element from the stack:')
print(stack.pop())
print(stack)

print('\nStack after elements are poped:')
print(stack)
